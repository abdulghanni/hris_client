<p>
    <strong> </strong>
    Langkah Mengajukan promosi, demosi &amp; mutasi :
</p>
<p>
    1. Langkah untuk melakukan promosi, demosi &amp; mutasi sama, hanya berbeda di menu form yang dipilih.
</p>
<p>
    2. Tekan tombol “Add” yang ada di pojok kanan atas halaman.
</p>
<p>
    <strong><em> </em></strong>
    <em></em>
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/promosi/1.png')?>" /><br/>
    <em>Gambar 1</em>
</p>
<p>
    3. Di halaman input pengajuan, isi informasi yang akan diajukan dan atasan yang dibutuhkan untuk melakukan approval.
</p>
<p>
    4. <strong> </strong>Jika Anda akan menambahkan lampiran tekan tombol browse untuk mengupload lampiran, jika lampiran lebih dari satu, tekan tombol “add
    attachment” untuk menambahkan lampiran lainnya.
</p>
<p>
    5. Jika data sudah terisi tekan tombol save untuk menyimpan data dan mengirim notifikasi ke atasan dan HRD untuk melakukan approval.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/promosi/2.png')?>" /><br/>
