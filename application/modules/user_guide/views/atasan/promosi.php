    Keterangan Gambar 2 :
</p>
<p>
    1. Tombol untuk mengunggah lampiran
</p>
<p>
    2. Tombol untuk menambah kolom lampiran / attachment.
</p>
<p>
    6. Jika data sudah tersimpan dan notifikasi sudah terkirim, maka Anda akan diarahkan ke halaman daftar pengajuan.
</p>
<p>
    <strong> </strong>
