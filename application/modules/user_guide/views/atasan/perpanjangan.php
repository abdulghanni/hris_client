<p>
    <strong>Mengajukan Perpanjangan Kontrak.</strong>
</p>
<p>
    Langkah mengajukan perpanjangan kontrak.
</p>
<p>
    1. Masuk ke halaman perpanjangan kontrak, lalu tekan tombol add yang ada di pojok kanan atas halaman untuk membuat pengajuan perpanjangan kontrak baru.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/perpanjangan/1.png')?>" /><br/>
    <em>Gambar 1</em>
</p>
<p>
    2. Pada form input, isi data sesuai pengajuan perpanjangan kontrak yang akan diajukan lalu pilih atasan yang dibuthkan untuk melakukan approval.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/perpanjangan/2.png')?>" /><br/>
    <em>Gambar 2</em>
</p>
<p>
    3. Setelah semua data terisi, tekan tombol save maka data pengajuan akan tersimpan dan akan mengirim notifikasi ke atasan untuk melakukan approval.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/perpanjangan/3.png')?>" /><br/>
    <em>Gambar 3</em>
</p>
<p>
    4. Saat atasan telah melakukan approval, maka Anda akan mendapatkan notifikasi.
</p>
<p>
    5. Tekan icon surat/notifikasi untuk melihat notifikasi, lalu tekan judul notifikasi untuk melihat detail status form.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/perpanjangan/4.png')?>" /><br/>
    <em> Gambar 4</em>
</p>
<p>
    6. pada detail email, tekan tautan â€œKlik Disiniâ€ untuk menuju detail form pengajuan.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/perpanjangan/5.png')?>" /><br/>
    <em>Gambar 5</em>
</p>
<p>
    7. Pada detail fom terlihat status terakhir pengajuan Anda.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/perpanjangan/6.png')?>" /><br/>
