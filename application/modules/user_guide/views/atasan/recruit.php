<p>
    <strong>Pengajuan Permintaan SDM Baru</strong>
</p>
<p>
    Langkah mengajukan permintaan SDM Baru :
</p>
<p>
    1. Buka menu Permintaan SDM.
</p>
<p>
    2. Tekan tombol add yang ada di pojok atas kanan halaman.
</p>
<p align="center">
    <em>Gambar 1.1</em>
</p>
<p>
    3. Di halaman input permintaan SDM Baru, isi data sesuai permintaan yang akan diajukan lalu pilih atasan yang diperlukan untuk approval pengajuan.
</p>
<p>
    4. Jika data sudah terisi dan atasan sudah dipilih maka tekan tombol save untuk menyimpan data dan mengirim permintaan approval ke atasan dan HRD.
</p>
<p>
    5. Jika data sudah tersimpan dan notifikasi ke atasan dan HRD sudah terkirim maka Anda akan diarahkan ke halaman depan Permintaan SDM.
</p>
<p align="center">
    <em>Gambar 1.2</em>
</p>
<p align="center">
    <em> </em>
</p>
<p align="center">
    <em>Gambar 1.3</em>
</p>
<p>
    6. Saat atasan sudah melakukan approval, maka Anda akan mendapatkan notifikasi.
</p>
<p>
    7. Pilih button notifikasi untuk melihat notifikasi yang masuk.
</p>
<p>
    8. Lalu tekan pada judul email untuk melihat detail status approval dari atasan.
</p>
<p align="center">
    <em>Gambar 1.4</em>
</p>
<p>
