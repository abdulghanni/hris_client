<p>
    <strong>Rekomendasi Karyawan Keluar</strong>
</p>
<p>
    Langkah mengajukan rekomendasi karyawan keluar:
</p>
<p>
    1. A. Jika tipe rekomendasi karyawan keluar <strong>resign/pengunduran diri</strong>, Anda sebagai atasan langsung dari karyawan yang mengajukan
    pengunduran diri akan mendapatkan notifikasi setelah pengajuan pengunduran diri karyawan tersebut disetujui HRD dan telah melakukan wawancara
    sebelumnya.pada detail notifikasi tekan tautan “klik disini” untuk menginput form rekomendasi karyawan keluar.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/1.png')?>" /><br/>
    <em> Gambar 1</em>
    <em></em>
</p>
<p>
    B. Jika tipe rekomendasi karyawan keluar selain resign /pengunduran diri, masuk ke halaman rekomendasi karyawan keluar, lalu tekan tombol add yang ada di
    pojok kanan atas halaman daftar rekomendasi karyawan keluar.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/2.png')?>" /><br/>
    <em> Gambar 2</em>
    <em></em>
</p>
<p>
    2. Pada form input, isi data sesuai pengajuan rekomendasi karyawan keluar yang akan diajukan lalu pilih atasan yang dibutuhkan untuk melakukan approval.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/3.png')?>" /><br/>
    <em>Gambar 3</em>
    <em></em>
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/4.png')?>" /><br/>
    <em> Gambar 4</em>
    <em></em>
</p>
<p>
    3. Setelah semua data terisi, tekan tombol save maka data pengajuan akan tersimpan dan akan mengirim notifikasi ke atasan untuk melakukan approval.
</p>
<p>
    4. Saat atasan telah melakukan approval, maka Anda akan mendapatkan notifikasi.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/5.png')?>" /><br/>
    <em> Gambar 5</em>
    <em></em>
</p>
<p>
    5. Tekan icon surat/notifikasi untuk melihat notifikasi, lalu tekan judul notifikasi untuk melihat detail status form.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/6.png')?>" /><br/>
    <em> Gambar 6</em>
    <em></em>
</p>
<p>
    6. pada detail email, tekan tautan “Klik Disini” untuk menuju detail form pengajuan.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/7.png')?>" /><br/>
    <em> Gambar 7</em>
    <em></em>
</p>
<p>
    7. Pada detail fom terlihat status terakhir pengajuan Anda.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/rekomendasi/8.png')?>" /><br/>
    <em>Gambar 8</em>
    <em></em>
