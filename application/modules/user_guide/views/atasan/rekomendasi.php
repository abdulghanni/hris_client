Langkah mengajukan rekomendasi karyawan keluar:

1.	A. Jika tipe rekomendasi karyawan keluar resign/pengunduran diri, Anda sebagai atasan langsung dari karyawan yang mengajukan pengunduran diri akan mendapatkan notifikasi setelah pengajuan pengunduran diri karyawan tersebut disetujui HRD dan telah melakukan wawancara sebelumnya.pada detail notifikasi tekan tautan “klik disini” untuk menginput form rekomendasi karyawan keluar.

B. Jika tipe rekomendasi karyawan keluar selain resign /pengunduran diri, masuk ke halaman rekomendasi karyawan keluar, lalu tekan tombol add yang ada di pojok kanan atas halaman daftar rekomendasi karyawan keluar,

2.	Pada form input, isi data sesuai pengajuan rekomendasi karyawan keluar yang akan diajukan lalu pilih atasan yang dibuthkan untuk melakukan approval.
3.	Setelah semua data terisi, tekan tombol save maka data pengajuan akan tersimpan dan akan mengirim notifikasi ke atasan untuk melakukan approval.
4.	Saat atasan telah melakukan approval, maka Anda akan mendapatkan notifikasi.
5.	Tekan icon surat/notifikasi untuk melihat notifikasi, lalu tekan judul notifikasi untuk melihat detail status form.
6.	pada detail email, tekan tautan “Klik Disini” untuk menuju detail form pengajuan.
7.	Pada detail fom terlihat status terakhir pengajuan Anda.
8.	Untuk mencetak form tekan tombol cetak pada detail form atau pada halaman daftar pengajuan.
