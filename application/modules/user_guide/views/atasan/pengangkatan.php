<p>
    <strong>Mengajukan Pengangkatan / Perubahan Status Karyawan.</strong>
</p>
<p>
    Langkah mengajukan pengangkatan.
</p>
<p>
    1. Masuk ke halaman pengangkatan, lalu tekan tombol add yang ada di pojok kanan atas halaman untuk membuat pengajuan pengangkatan baru.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/pengangkatan/1.png')?>" /><br/>
    <em>Gambar 1</em>
</p>
<p>
    2. Pada form input, isi data sesuai pengajuan pengangkatan yang akan diajukan lalu pilih atasan yang dibuthkan untuk melakukan approval.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/pengangkatan/2.png')?>" /><br/>
    <em>Gambar 2</em>
    <em></em>
</p>
<p>
    3. Setelah semua data terisi, tekan tombol save maka data pengajuan akan tersimpan dan akan mengirim notifikasi ke atasan untuk melakukan approval.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/pengangkatan/3.png')?>" /><br/>
    <em>Gambar 3</em>
    <em></em>
</p>
<p>
    4. Saat atasan telah melakukan approval, maka Anda akan mendapatkan notifikasi.
</p>
<p>
    5. Tekan icon surat/notifikasi untuk melihat notifikasi, lalu tekan judul notifikasi untuk melihat detail status form.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/pengangkatan/4.png')?>" /><br/>
    <em>Gambar 4</em>
    <em></em>
</p>
<p>
    6. pada detail email, tekan tautan “Klik Disini” untuk menuju detail form pengajuan.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/pengangkatan/5.png')?>" /><br/>
    <em>Gambar 5</em>
    <em></em>
</p>
<p>
    7. Pada detail form terlihat status terakhir pengajuan Anda.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/atasan/pengangkatan/6.png')?>" /><br/>
    <em>Gambar 6</em>
    <em></em>
</p>
<p>
    8. Untuk mencetak form tekan tombol cetak pada detail form atau pada halaman daftar pengajuan.
</p>
