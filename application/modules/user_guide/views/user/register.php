<p>
    <h3>Registrasi Akun Web HRIS Erlangga</h3>
</p>
<p>
    <strong> </strong>
    Untuk melalakukan registrasi akun di Web HRIS Erlangga, pertama masuk ke halaman depan Web HRIS Erlangga, lalu masukan NIK Anda pada kolom NIK dan pada
    kolom password masukan ‘password’(tanpa tanda petik). Password tersebut hanya digunakan pada saat pertama kali registrasi.
</p>
<p align="center">
	<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/register/1.png')?>" /><br/>
    <em>Gambar 1</em>
</p>
<p>
    Jika sudah mengisi kolom NIK dan Password, tekan tombol login lalu tunggu beberapa saat sampai muncul notifikasi status login Anda.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/register/2.png')?>" /><br/>
    <em>Gambar 2</em>
</p>
<p>
    Jika NIK dan password yang Anda masukan benar, maka akan muncul pesan “Account is Inactive”. Notifikasi bahwa Anda telah melakukan registrasi telah
    terkirim ke Administrator untuk dilakukan verifikasi dan pengaktifan akun.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/register/3.png')?>" /><br/>
    <em>Gambar 3</em>
</p>
<p>
    Saat administrator mengaktifkan akun Anda, maka Anda akan menerima notifikasi ke akun email coorporate Erlangga anda, jika Anda tidak memiliki akun email
    coorporate Erlangga, maka notifikasi akan dikirimkan ke email pribadi Anda yang tersimpan didatabase Erlangga, Jika kedua email Anda kosong silakan hubungi
    Administrator untuk mengetahui status aktivasi akun Anda.
</p>
<p>
    Berikut contoh tampilan notifikasi status pengaktifan akun yang dikirm ke email pribadi :
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/register/4.png')?>" /><br/>
    <em>Gambar 4</em>
</p>
<p>
Jika status akun Web HRIS Erlangga Anda sudah aktif, silakan lakukan kembali login seperti pada saat registrasi dengan memasukan nik dan password. 
</p>
