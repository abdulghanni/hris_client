<p>
    Keterangan Gambar 4 :
</p>
<p>
    1. Status approval Atasan &amp; HRD :
</p>
<p>
    - Icon (Tanda tanya) adalah simbol bahwa pengajuan Anda belum mendapat tanggapan.
</p>
<p>
    - Icon (Checklist) adalah simbol bahwa pengajuan Anda telah disetujui.
</p>
<p>
    - Icon (Silang) adalah simbol bahwa pengajuan Anda ditolak.
</p>
<p>
    - Icon (Tanda Seru) adalah simbol bahwa pengajuan Anda pending.
</p>
<p>
    - Icon (Strip) adalah simbol bahwa pada posisi tersebut tidak membutuhkan. approval.
</p>
<p>
    2. Tombol untuk mencetak form pengajuan.
</p>
<p>
    6. Jika atasan sudah melakukan approval maka Anda akan mendapatkan notifikasi baru, tekan tombol notifikasi, lalu Anda akan diarahkan ke halaman Email, dan
    melihat status pengajuan dari atasan, tekan di pesan tersebut maka Anda akan melihat detail email.
</p>
