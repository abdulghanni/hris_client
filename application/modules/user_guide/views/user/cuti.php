    <a name="OLE_LINK1">Langkah mengajukan Cuti :</a>
</p>
<p>
    1. Masuk ke menu daftar pengajuan cuti
</p>
<p>
    2. Tekan tombol add yang ada di pojok kanan atas halaman daftar pengajuan cuti
</p>

