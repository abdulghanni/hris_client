<p>
    Keterangan gambar 1:
</p>
<p>
    1. Tombol untuk menambah pengajuan cuti baru.
</p>
<p>
    3. Isi Semua data yang dibutuhkan pada form Input Cuti dan pilih atasan yang diperlukan untuk approval(Untuk HRD sudah otomatis terisi, jadi tidak perlu
    memilih approval untuk HRD), atasan langsung wajib diisi, sedangkan atasan langsung dan lainnya diisi hanya jika diperlukan.
</p>
<p>
    4. Jika semua data sudah terisi tekan tombol save untuk menyimpan dan mengirim notifikasi approval ke atasan dan HRD.
</p>
