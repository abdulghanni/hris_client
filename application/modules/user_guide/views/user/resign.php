
<p>
    <strong>Pengajuan Pengunduran Diri</strong>
</p>
<p>
    Langkah mengajukan pengunduran diri :
</p>
<p>
    1. Masuk ke menu pengajuan pengunduran diri.
</p>
<p>
    2. Tekan tombol add ang ada di pojok kanan atas halaman.
</p>
<p align="center">
    <img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/1.png')?>" /><br/>
    <a name="OLE_LINK25"><em>Gambar 1</em></a>
</p>
<p align="center">
    <em> </em>
</p>
<p>
    3. Pada form input pengajuan pengunduran diri isi data sesuai dengan informasi yang dibutuhkan dan pilih atasan yang diperlukan untuk melakukan approval.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/2.png')?>" /><br/>
    <em>Gambar 2</em>
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/3.png')?>" /><br/>
    <em>Gambar 3</em>
</p>
<p>
    4. Jika sudah mengisi data yang dibuthkan tekan tombol save, maka data pengajuan akan tersimpan dan notifikasi akan terkirim keatasan yang sudah dipilih
    sebulumnya.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/4.png')?>" /><br/>
    <em>Gambar 4</em>
</p>
<p align="center">
    <em> </em>
</p>
<p>
    5. Saat atasan melakukan approval maka Anda akan mendapatkan notifikasi.
</p>
<p>
    6. Buka notifikasi tersebut dan Anda akan diarahkan ke detail notifikasi.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/5.png')?>" /><br/>
    <em>Gambar 5</em>
</p>
<p>
    7. Tekan tautan “klik disini”, maka akan diarahkan ke detail pengajuan yang telah Anda buat.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/6.png')?>" /><br/>
    <em>Gambar 6</em>
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/7.png')?>" /><br/>
    <em>Gambar 7</em>
</p>
<p>
    8. Setelah atasan melakukan approval maka HRD akan mengundang Anda untuk melakukan wawancara, saat HRD mengundang Anda untuk melakukan wawancara maka akan
    muncul notifikasi undangan wawancara.
</p>
<p>
    9. Tekan pada judul notifikasi tersebut untuk melihat detail pesan dari HRD.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/8.png')?>" /><br/>
    <a name="OLE_LINK33"><em>Gambar 8</em></a>
    <em></em>
</p>
<p>
    10. Pada detail email, tekan tautan “klik disini untuk melihat detail form pengajuan beserta informasi undangan dari HRD.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/9.png')?>" /><br/>
    <em>Gambar 9</em>
</p>
<p>
    <em> </em>
</p>
<p>
    11. Di detail form pengajuan Anda sekarang berisi pengajuan yang telah Anda buat beserta informasi undangan wawancara dari HRD.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/10.png')?>" /><br/>
    <em>Gambar 10</em>
</p>
<p align="center">
    <em> </em>
</p>
<p>
    12. Ketika Anda melakukan wawancara, maka HRD akan menanyakan beberapa informasi yang dibutuhkan, seperti pada contoh gambar berikut.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/11.png')?>" /><br/>
    <em>Gambar 11</em>
</p>
<p>
    13. Setelah wawancara selesai maka Anda dapat melihat detail wawancara yang sudah dilakukan di detail form pengajuan pengunduran diri yang Anda buat.
</p>
<p>
    14. Setelah selesai maka proses pengunduran diri Anda akan dilakukan oleh atasan Anda melalui form rekomendasi karyawan keluar.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/resign/12.png')?>" /><br/>
    <em>Gambar 12</em>
</p>
<p>
    15. Untuk mencetak form pengajuan pengunduran diri Anda, silakan tekan tombol cetak pada detail form atau pada icon cetak di daftar pengajuan pengunduran
    diri.
</p>
