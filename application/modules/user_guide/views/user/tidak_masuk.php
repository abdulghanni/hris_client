<p>
    <strong>Izin Tidak Masuk</strong>
</p>
<p>
    Langkah mengajukan Izin tidak masuk:
</p>
<p>
    1. Masuk ke menu daftar pengajuan Izin tidak masuk
</p>
<p>
    2. Tekan tombol add yang ada di pojok kanan atas halaman daftar Izin tidak masuk
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/tidak_masuk/1.png')?>" /><br/>
    <em>Gambar 1</em>
</p>
<p>
    3. Pada halaman input izin tidak masuk, isi semua data keterangan tidak masuk dan atasan yang dibutuhkan untuk melakukan approval.
</p>
<p>
    4. Jika semua sudah terisi, tekan tombol save untuk menyimpan data dan mengirim notifikasi ke atasan dan HRD.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/tidak_masuk/2.png')?>" /><br/>
    <em>Gambar 2</em>
</p>
<p>
    5. Jika data telah tersimpan Anda akan diarahkan kehalaman daftar izin tidak masuk.
</p>
<p>
    6. Saat atasan melakukan approval, maka akan muncul notifikasi, tekan tombol notifikasi untuk melihat status approval dati atasan.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/tidak_masuk/3.png')?>" /><br/>
    <em>Gambar 3</em>
</p>
<p>
    7. Di daftar email tekan judul email untuk melihat detail email yang diterima.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/tidak_masuk/4.png')?>" /><br/>
    <em>Gambar 4</em>
</p>
<p>
    8. Lalu tekan tautan klik disini untuk mengarahkan ke form keterangan tidak masuk.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/tidak_masuk/5.png')?>" /><br/>
    <em>Gambar 5</em>
</p>
<p>
    9. Pada form keterangan tidak masuk, terlihat status terakhir approval form yang Anda ajukan.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/user/tidak_masuk/6.png')?>" /><br/>
    <em>Gambar 6</em>
</p>
<p>
    10. Untuk mencetak form, tekan tombol cetak di detail form atau pada indeks daftar izin tidak masuk.
</p>
