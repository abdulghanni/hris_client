
<p>
    <strong>Mengisi dan Mengubah Data Inventaris Karyawan</strong>
</p>
<p>
    Saat karyawan berhasil melakukan registrasi di Web-HRIS Erlangga, maka tiap user/karyawan yang ditunjuk sebagai admin inventaris akan mendapatkan
    notifikasi untuk mengisi data inventaris karyawan.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_inventaris/1.png')?>" /><br/>
    <em>Gambar 1</em>
</p>
<p>
    Tekan pada judul notifikasi tersebut, pada detail notifikasi tersebut terdapat tautan yang mengarahkan ke halaman untuk mengisi data inventaris.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_inventaris/2.png')?>" /><br/>
    <em>Gambar 2</em>
</p>
<p>
    Pada halaman input inventaris, isi inventaris sesuai yang dimiliki oleh karyawan, jika diperlukan isi approval atasan untuk mengetahui inventaris karyawan
    tersebut.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_inventaris/3.png')?>" /><br/>
    <em>Gambar 3</em>
</p>
<p>
    Jika sudah tersimpan Anda dapat merubahnya dengan menekan tombol update inventaris. Pada form update inventaris isi perubahan yang data inventaris, jika
    diperlukan isi juga approval atasan Anda.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_inventaris/4.png')?>" /><br/>
    <em>Gambar 4</em>
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_inventaris/5.png')?>" /><br/>
    <em>Gambar 5</em>
</p>
<p>
    Untuk melihat semua daftar inventaris karyawan dalam cabang Anda, masuk ke menu pengaturan-&gt;inventaris, karyawan yang sudah diisi data inventarisnya,
    pada kolom submit ditandai dengan simbol “ceklist”, jika belum diisi data inventarisnya kolom field akan berisi simbol “silang”.
</p>
<p align="center">
<img class="img-responsive" src="<?= assets_url('user_guide/admin_inventaris/6.png')?>" /><br/>
    <em>Gambar 6</em>
