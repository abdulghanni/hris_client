<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_bagian/medical/3.png')?>" /><br/>
    <em>Gambar 3</em>
</p>
<p>
    5. Saat atasan melakukan approval maka akan muncul notifikasi di menu email.
</p>
<p>
    6. Tekan pada judul email untuk melihat detail email
</p>
<p>
    7. Tekan pada tautan “klik disini” untuk menuju form dan melihat status approval terakhir.
</p>
