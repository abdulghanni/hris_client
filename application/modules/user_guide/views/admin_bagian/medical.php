<p>
    <strong>3. </strong>
    <strong>Pengajuan Rekap Kesehatan / Medical</strong>
</p>
<p>
    Langkah mengajukan pelatihan :
</p>
<p>
    1. Masuk ke menu daftar pengajuan Kesehatan
</p>
<p>
    2. Tekan tombol add yang ada di pojok kanan atas halaman daftar Kesehatan
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_bagian/medical/1.png')?>" /><br/>
    <a name="OLE_LINK89"><em>Gambar 1</em>
<p>
    3. Isi Semua data yang dibutuhkan pada form Input Kesehatan dan pilih atasan yang diperlukan untuk approval(Untuk HRD sudah otomatis terisi, jadi tidak
    perlu memilih approval untuk HRD), atasan langsung wajib diisi, sedangkan atasan langsung dan lainnya diisi hanya jika diperlukan.
</p>
<p>
    4. Jika semua data sudah terisi tekan tombol save untuk menyimpan data dan mengirim permintaan approval ke atasan dan HRD.
</p>
<p align="center">
<img class="img-responsive" height="500px" width="750px" src="<?= assets_url('user_guide/admin_bagian/medical/2.png')?>" /><br/>
    <em>Gambar 2</em>
</p>
