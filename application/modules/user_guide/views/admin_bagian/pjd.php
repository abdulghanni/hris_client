<p>
    2. Tombol untuk menghapus karyawan pelaksana tugas perjalanan dinas.
</p>
<p>
    3. Tombol untuk menambah biaya perjalanan dinas
</p>
<p>
    4. Tombol untuk menyimpan data, dan menuju ke form input biaya
</p>
<p>
    5. Isi Biaya perjalanan dinas di form input biaya,untuk mengetahui total biaya perjalanan dinas tekan tombol hitung, lalu tekan tombol save untuk menyimpan
    biaya perjalanan dinas dan mengirim notifikasi ke atasan dan HRD.
</p>
