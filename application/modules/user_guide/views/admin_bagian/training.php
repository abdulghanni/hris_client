    <a name="OLE_LINK77"></a>
    <a name="OLE_LINK76"><em>Gambar 2.1</em></a>
</p>
<p>
    3. Isi Semua data yang dibutuhkan pada form Input Pelatihan dan pilih atasan yang diperlukan untuk approval(Untuk HRD sudah otomatis terisi, jadi tidak
    perlu memilih approval untuk HRD), atasan langsung wajib diisi, sedangkan atasan langsung dan lainnya diisi hanya jika diperlukan.
</p>
<p>
    4. Jika semua data sudah terisi tekan tombol save untuk menyimpan data dan mengirim permintaan approval ke atasan dan HRD.
</p>
