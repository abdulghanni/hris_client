<p>
    Langkah mengajukan pelatihan :
</p>
<p>
    1. Masuk ke menu daftar pengajuan pelatihan
</p>
<p>
    2. Tekan tombol add yang ada di pojok kanan atas halaman daftar pelatihan
</p>
<p align="center">
    <a name="OLE_LINK81"></a>
    <a name="OLE_LINK80"></a>
    <a name="OLE_LINK79"></a>
