<p>
    <strong>Pengaturan Group</strong>
</p>
<p>
    Menu pengaturan Group adalah menu yang digunakan untuk mengelola group-group yang akan mengatur otoritas user-user tertentu, sesuai group yang telah dibuat
    sebelumnya.
</p>
<p>
    Untuk menambahkan group baru masuk ke menu pengaturan-&gt;group, lalu tekan tombol add.
</p>
