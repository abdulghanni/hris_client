<p>
    Pada halaman tambah group, isi nama group dan keterangannya. Untuk tipe group dibagi menjadi 4, yaitu :
</p>
<p>
    - Super administrator : administrator yang memiliki hak penuh untuk melakukan pengisian di semua form, serta pengaturan aplikasi web-HRIS
</p>
<p>
    - Admin Departemen / Bagian : administrator yang dapat melakukan pengajuan pada form-form tertentu, atas nama dirinya atau atasan dengan departement yang
    sama.
</p>
<p>
    - Admin Inventaris : administrator yang mengatur data inventaris dari setiap karyawan dalam satu cabang.
</p>
<p>
    - Admin Lain-lain : administrator tambahan yang dibutuhkan untuk melakukan approval di beberapa form tertentu, seperti admin payroll, admin legal dsb.
</p>
