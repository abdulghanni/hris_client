<p>
    <strong>Pengaturan Admin Khusus</strong>
</p>
<p>
    <strong> </strong>
</p>
<p>
    Admin khusus adalah karyawan diluar PT. Erlangga atau karyawan yang tidak memiliki NIK Di PT Erlangga, namun ditunjuk sebagai admin untuk membuat pengajuan
    di departement tertentu.
</p>
<p>
    Untuk menambahkan admin khusus pilih menu Admin khusus pada pengaturan perusahaan.
</p>
<p align="center">
    <em>Gambar2.1</em>
</p>
<p>
    Untuk menambahkan admin khusus baru, tekan tombol add
</p>
<p align="center">
    <em>Gambar 2.2</em>
</p>
<p>
    Lalu akan muncul form untuk mengisi data admin tersebut.
</p>
<p align="center">
    <em>Gambar 2.3</em>
</p>
<p>
    Isi seluruh data yang dibutuhkan, lalu tekan tombol save untuk menyimpan data.
</p>
<p align="center">
    <em>Gambar 2.4</em>
</p>
<p>
    Setelah admin khusus dibuat, tambahkan otoritas sebagai admin khusus untuk memberikan akses ke menu-menu yang dapat diakses oleh admin departement /
    bagian.
</p>
<p>
    Untuk menambahkan group tekan tombol edit pada admin yang akan ditambahkan aksess group.
</p>
<p align="center">
    <em>Gambar 2.5</em>
</p>
<p>
    Pada halaman edit profile, pilih/centang pada group yang akan menjadi akses admin tersebut, Nama group dapat ditambah atau dirubah melalui menu
    Group,informasi lengkap mengenai pengaturan Group, silakan baca panduan menu Group.
</p>
<p align="center">
    <em>Gambar 2.6</em>
</p>
<p>
    Jika perubahan sudah dilakukan maka admin khusus dapat melakukan login dan mengakses menu-menu untuk admin khusus.
</p>
<p>
    Untuk menghapus admin khusus, tekan tombol delete pada bagian action.
</p>
<p align="center">
    <em>Gambar 2.7</em>
</p>