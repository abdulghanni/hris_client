<p>
    <strong>Form Template</strong>
</p>
<p>
    Form template adalah daftar template dari tiap form, yang dapat didownload oleh setiap user.
</p>
<p>
    Untuk melakukan perubahan daftar form template, masuk ke menu pengaturan-&gt;form template.
</p>
