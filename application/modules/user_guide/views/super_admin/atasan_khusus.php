<p>
    <strong>Approval Atasan Khusus</strong>
</p>
<p>
    Approval atasan khusus adalah daftar user yang diperlukan untuk approval di beberapa form tertentu yang membutuhkan approval dari atasan yang biasanya
    berada di cabang pusat, dan jika user/karyawan yang mengajukan diluar cabang pusat maka nama-nama atasan khusus tersebut dapat dipilih.
</p>
<p>
    Untuk melakukan perubahan daftar atasan khusus, masuk ke menu approval atasan khusus.
</p>
<p align="center">
    <em>Gambar 4.1</em>
</p>
<p>
    Untuk menambahkan atasan, tekan tombol add lalu pilih user yang akan ditambahkan sebagai atasan khusus.
</p>
