<p>
    <strong>Pengaturan Approval HRD</strong>
</p>
<p>
    Approval HRD adalah user dari HRD yang ditunjuk untuk melakukan approval di form-form tertentu, sesuai dengan pengaturan yang diatur di menu Pengaturan
    approval HRD.
</p>
<p>
    Untuk melakukan pengaturan Approval HRD, masuk ke menu pengaturan Approval HRD.
</p>
